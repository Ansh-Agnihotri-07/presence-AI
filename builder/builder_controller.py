def _check_feature_completeness(project_root: Path, goal: dict) -> list[str]:
    """
    Heuristic check that requested features are represented in code/UI.
    Returns list of warning strings. Empty = all features likely present.
    """
    features = goal.get("features", [])
    if not features:
        return []

    warnings = []
    features_str = " ".join(features).lower()

    # Read app.py if it exists
    app_py = project_root / "app.py"
    app_content = ""
    if app_py.exists():
        try:
            app_content = app_py.read_text(encoding="utf-8").lower()
        except Exception:
            pass

    # Read all HTML files
    html_content = ""
    templates_dir = project_root / "templates"
    if templates_dir.exists():
        for html_file in templates_dir.glob("*.html"):
            try:
                html_content += html_file.read_text(encoding="utf-8").lower() + "\n"
            except Exception:
                pass

    # Check for CRUD operations
    if any(kw in features_str for kw in ["add", "create", "new"]):
        if "post" not in app_content and "request.form" not in app_content:
            warnings.append("Feature 'add/create' requested but no POST handling found in app.py")

    if any(kw in features_str for kw in ["delete", "remove"]):
        if "delete" not in app_content and "remove" not in app_content:
            warnings.append("Feature 'delete/remove' requested but no delete logic found in app.py")

    if any(kw in features_str for kw in ["edit", "update"]):
        if "edit" not in app_content and "update" not in app_content:
            warnings.append("Feature 'edit/update' requested but no edit logic found in app.py")

    if any(kw in features_str for kw in ["filter", "search"]):
        if "filter" not in app_content and "search" not in app_content and "query" not in app_content:
            warnings.append("Feature 'filter/search' requested but no filter logic found")

    if any(kw in features_str for kw in ["summary", "overview", "dashboard"]):
        if "<h" not in html_content and "summary" not in html_content:
            warnings.append("Feature 'summary/dashboard' requested but no summary section found in HTML")

    # Check for forms in HTML if CRUD features requested
    if any(kw in features_str for kw in ["add", "create", "edit", "update"]):
        if "<form" not in html_content:
            warnings.append("CRUD features requested but no <form> found in HTML templates")

    return warnings


