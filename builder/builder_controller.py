        if not build_success:
            status_msg = "[ERROR] Project build FAILED. Check logs above."
        elif fallback_used:
            status_msg = "[DEGRADED] Project is RUNNABLE but used fallback deterministic scaffolds."
        elif feature_warnings:
            status_msg = "[PARTIAL SUCCESS] Project is running, but some requested features might be missing."

