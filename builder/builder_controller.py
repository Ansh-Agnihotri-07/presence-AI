                yield f"  [CHECK] Actual template files: {sorted(actual_templates)}"

                missing = found_refs - actual_templates
                if missing:
                    yield f"  [FAIL] Missing templates: {sorted(missing)}"
                    yield f"  >> Starting Route Harmonization repair on app.py..."

                    cmd_check = {
                        "status": "error",
                        "error_type": "missing_template",
                        "error_message": (
                            f"app.py references {sorted(missing)}, but only "
                            f"{sorted(actual_templates)} exist. "
                            "PATCH the Python routes to use ONLY the existing templates."
                        ),
                    }

                    repaired = False
                    async for repair_msg in repair_loop.run(
                        initial_check=cmd_check,
                        failed_command="Cross-file template reference validation",
                        project_root=project_root,
                        files_to_create=plan["files_to_create"],
                        target_path_override="app.py",
                        goal=goal,
                    ):
                        yield f"  {repair_msg}"
                        if repair_msg.startswith("[SUCCESS]"):
                            repaired = True

