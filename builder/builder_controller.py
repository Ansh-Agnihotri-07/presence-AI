def _validate_frontend_content(content: str, rel_path: str) -> bool:
    """Rigorous in-memory check for HTML/CSS/JS/README stubs."""
    stripped = content.strip()
    if not stripped or len(stripped) < 50:
        return False
        
    lower = stripped.lower()
    
    # README check
    if rel_path.lower() == "readme.md":
        if "auto-generated" in lower and len(stripped) < 150:
            return False
        return True

    # CSS requirements: root/body, layout, and interactive
    if rel_path.endswith(".css"):
        # Require body or :root
        if "body" not in lower and ":root" not in lower:
            return False
        # Require layout/container
        if not re.search(r"\.(?:container|main|wrapper|layout|grid|flex)", lower):
            return False
        # Require interactive (button, input, nav)
        if not re.search(r"(?:button|input|nav|a:|btn)", lower):
            return False
        # counts
        selector_count = len(re.findall(r"[a-zA-Z.#\[\]:*@][^{]*\{", stripped))
        property_count = len(re.findall(r":\s*[^;]+;", stripped))
        if selector_count < 5 or property_count < 10:
            return False
        return True

    # JS requirements: real constructs
    if rel_path.endswith(".js"):
        # Real constructs count
        js_constructs = 0
        for pattern in ["function ", "const ", "let ", "var ", "=>", "document.",
                         "addEventListener", "querySelector", "fetch(", "async "]:
            if pattern in content:
                js_constructs += 1
        return js_constructs >= 2

    return True


def _sanitize_python_code(content: str) -> tuple[str, bool, bool]:
    """
    Sanitize and extract Python code.
    Returns: (sanitized_content, extraction_applied, sanity_check_passed)
    """
    content = content.replace("\r\n", "\n")
    extraction_applied = False
    
    # Priority 1: Fenced blocks
    if "```python" in content.lower():
        match = re.search(r"```python\s*(.*?)```", content, re.DOTALL | re.IGNORECASE)
        if match:
            content = match.group(1).strip()
            extraction_applied = True
    elif "```" in content:
        match = re.search(r"```[\w]*\s*(.*?)```", content, re.DOTALL)
        if match:
            content = match.group(1).strip()
            extraction_applied = True

    # Priority 2: Scan from first valid construct if it seems to start with prose
    first_lines = "\n".join(content.splitlines()[:3]).lower()
    prose_blacklist = [
        "i'm having trouble", "here's the code", "here is the", "let me explain", 
        "this code", "this script", "to fix this", "the issue is"
    ]
    if not extraction_applied and any(p in first_lines for p in prose_blacklist) or not re.match(r'^(import |from |def |class |app =|@|#)', content.lstrip()):
        lines = content.splitlines()
        code_start = -1
        valid_starts = ("import ", "from ", "def ", "class ", "app =", "@app", "if __name__", "flask")
        for i, line in enumerate(lines):
            if any(line.lstrip().startswith(v) for v in valid_starts):
                code_start = i
                break
        
        if code_start != -1:
            content = "\n".join(lines[code_start:]).strip()
            extraction_applied = True
            
    # Priority 3: Reject sanity check if still no constructs or conversational
    sanity_passed = True
    lower_content = content.lower()
    constructs = ["import ", "def ", "class ", "app =", "@app.route", "flask"]
    if not any(c in lower_content for c in constructs):
        sanity_passed = False
    if any(p in lower_content[:200] for p in prose_blacklist):
        sanity_passed = False

    return content, extraction_applied, sanity_passed


