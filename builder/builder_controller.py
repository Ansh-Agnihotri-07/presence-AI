            yield f"  >> Starting repair loop..."

            repaired = False
            async for repair_msg in repair_loop.run(
                initial_check=cmd_check,
                failed_command=cmd,
                project_root=project_root,
                files_to_create=plan["files_to_create"],
                goal=goal,
            ):
                yield f"  {repair_msg}"
                if repair_msg.startswith("[SUCCESS]"):
                    repaired = True

