async def _generate_file_content(
    description: str,
    project_name: str,
    tech_stack: str,
    user_request: str,
) -> str:
    """Ask LLM to generate the content for a single project file."""
    try:
        from ai.ai_router import route_llm
        system = (
            "You are an expert code generator producing premium-quality application code. "
            "Output ONLY the raw file content - no explanation, no preamble, no markdown fences, no filename header.\n\n"
            "CRITICAL RULES:\n"
            "1. Scripts must be fully self-contained and runnable with zero CLI arguments.\n"
            "2. Never use sys.argv or require command-line input. Use hardcoded defaults.\n"
            "3. Follow the user's EXACT request. Replicate specified behavior EXACTLY.\n"
            "4. DO NOT embed large HTML/CSS/JS blocks inside Python files.\n"
            "5. Assume separate files exist for templates and static assets.\n\n"
            "HTML RULES:\n"
            "- Every .html file MUST be a COMPLETE STANDALONE HTML5 document.\n"
            "- Required: <!DOCTYPE html>, <html>, <head> with <title> and <meta viewport>,\n"
            "  <body> with real content, link to /static/css/style.css,\n"
            "  link to Google Fonts (Inter or Roboto), <script src='/static/js/app.js'>.\n"
            "- Include: <nav> with app name, <main> with card-based layouts,\n"
            "  <footer> with copyright.\n"
            "- DO NOT use {% extends %} or {% block %} or Jinja2 template inheritance.\n"
            "- Use Jinja2 ONLY for dynamic data: {{ variable }}, {% for %}, {% if %}.\n"
            "- DO NOT output fragments or placeholder pages.\n\n"
            "CSS RULES:\n"
            "- Use CSS custom properties (variables) for colors and spacing.\n"
            "- Include: reset/normalize, container max-width, nav styling, card components\n"
            "  with box-shadow and border-radius, button hover/focus states with transitions,\n"
            "  input/form styling, responsive @media queries, typography with Google Fonts.\n"
            "- NEVER output placeholder CSS. Must be 200+ lines of real styles.\n\n"
            "PYTHON/FLASK RULES:\n"
            "- Use render_template() for every page route.\n"
            "- Use JSON file persistence (data/*.json) unless database explicitly requested.\n"
            "- Include proper error handling.\n"
            "- DO NOT embed HTML strings in Python.\n\n"
            "QUALITY STANDARD: Intermediate-to-premium. Think presentable demo quality.\n"
            "NOT: toy scaffold, barebones form dump, placeholder pages, minimal prototype."
        )
        prompt = (
            f"ORIGINAL USER REQUEST (follow exactly): {user_request}\n\n"
            f"File to generate: {description}\n"
            f"Project: {project_name}  Tech stack: {tech_stack}\n"
            "Rules:\n"
            "- Premium-quality, non-toy implementation\n"
            "- Modern UI aesthetics with cards, shadows, rounded corners\n"
            "- Runs with zero CLI arguments\n"
            "- Must match the user's request exactly\n"
            "- HTML files must be COMPLETE standalone documents (no Jinja extends)\n"
            "- Output ONLY the file content, no markdown fences, no filename"
        )
        content, _ = await route_llm(system_prompt=system, user_message=prompt, mode="tech")

        # Strip markdown fences if LLM wrapped the output
        if "```" in content:
            match = re.search(r"```[\w]*\n(.*?)```", content, re.DOTALL)
            if match:
                content = match.group(1).strip()
            else:
                content = re.sub(r"```[\w]*\n?", "", content)
                content = content.replace("```", "").strip()
        else:
            content = content.strip()

        # Python sanitization
        if description.endswith(".py"):
            raw_len = len(content)
            content, extraction_applied, sanity_passed = _sanitize_python_code(content)
            logger.info(f"[PY_GEN] Length={raw_len} -> {len(content)} | Extracted: {extraction_applied} | Sanity: {sanity_passed}")
            if not sanity_passed:
                logger.info(f"[PY_GEN] Output failed sanity check (prose/no code). Regenerating with fallback.")
                if "app.py" in description:
                    return build_deterministic_flask_app()
                return "# Failed to generate script (output was not Python code)\npass\n"

        # Sanity check for README
        if description.lower().endswith("readme.md") or "readme" in description.lower():
            if not _validate_frontend_content(content, "README.md"):
                logger.info("[DOCS_GEN] README fails sanity check (is placeholder). Using fallback.")
                return _FALLBACK_README

        return content
    except Exception as e:
        logger.error(f"[ERROR] File content generation failed for {description!r}: {e}")
        return f"# Auto-generated placeholder\n# Description: {description}\n"


